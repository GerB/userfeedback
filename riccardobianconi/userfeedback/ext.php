namespace riccardobianconi\userfeedback;

/**
* Extension class for custom enable, disable or purge actions
*/
class ext extends \phpbb\extension\base
{
	/**
	 * Check if the extension can be enabled
	 *
	 * @return bool
	 * @access public
	 */
	public function is_enableable()
	{
		$config = $this->container->get('config');
		return phpbb_version_compare($config['version'], '3.1.8', '>=');
	}
}
